	<p><strong>Carga promedio (Ton)</strong></p>

	<p>Ingrese la carga útil promedio (en toneladas) para cada empresa transportita. La carga útil representa el <strong>peso promedio de carga</strong> por viaje cargado. Las estimaciones de carga útil deben incluir el peso de la tarima y del embalaje. </p>

	<p>Transportistas no-PTL: En caso de que tenga dos o más empresas transportistas no-PTL, en una misma categoría del Programa Transporte Limpio, la carga promedio a reportar deberá ser el peso promedio de carga por viaje de todos sus transportistas no-PTL (que tienen la misma categoría).</p>

