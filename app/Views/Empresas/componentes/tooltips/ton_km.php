	<p><strong>Toneladas-Kilómetro (Ton-Km)</strong></p>
	<p>Ingrese las toneladas por kilómetro transportadas por año de cada una de las empresas de transporte que contrata</p>
	<p>Una tonelada-kilómetro es una tonelada moviéndose un kilómetro. NO ESTIME SUS TONELADAS-KILÓMETROS SIMPLEMENTE MULTIPLICANDO KILÓMETROS TOTALES POR LAS TONELADAS TOTALES. Este cálculo supone efectivamente que su tonelaje completo se transporta en TODOS Y CADA UNO de los camiones y sobrestimará sus toneladas-kilómetros.</p>
	<p>Hay dos formas que puede usar para calcular las toneladas-kilómetro por transportista:</p>
	<p> 1. Si conoce la carga útil promedio por transportista: multiplique la carga útil promedio por el 
		total de kilómetros por transportista.
	</p>
	<p>2. Si no conoce la carga útil promedio por transportista puede calcular las toneladas-kilómetros de la siguiente forma: </p>


	<br/><br/>
	<table style="font-size:.9em;">
		<tr>
			<td rowspan="2">
				Tonelada-kilómetro por transportista =
			</td>
			<td style="border-bottom: solid 1px;text-align: center;">
				(<strong>Total de kilómetros</strong> por transportista x <strong>Total de toneladas</strong> por transportista)
			</td>
		</tr>
		<tr>
			<td style="text-align: center;">
				<strong>Total de número de viajes</strong> por transportista
			</td>
		</tr>
	</table>
	<br/><br/>
	<p>NOTA: Los kilómetros en vacío no están Incluidos en el cálculo de las tonelada-kilómetro]</p>
	<p>Para verificar su cálculo, puede dividir sus toneladas-kilómetro entre los kilómetros. El resultado es su carga útil promedio de la flota. SI este número no es razonable, por favor verifique sus cálculos.</p>

	<p>Transportistas no-PTL: En caso de que tenga dos o más empresas transportistas no-PTL, en una misma categoría del Programa Transporte Limpio, las Ton-Km a reportar se deberán  calcular multiplicando la carga útil promedio de todos sus transportistas no-PTL (que tienen la misma categoría) por los kilómetros totales de todos sus transportistas no-PTL (que tienen la misma categoría).</p>

