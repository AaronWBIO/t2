<p><strong>Tipo de contacto</strong></p>
<p>Cada contacto puede ser designado como m&aacute;s de un tipo. Por ejemplo, su contacto de trabajo tambi&eacute;n puede ser un contacto de sostenibilidad, o uno de sus otros contactos tambi&eacute;n puede ser un contacto ejecutivo o de trabajo.&nbsp;</p>
<p>Seleccione las casillas de verificaci&oacute;n para indicar el tipo de contacto.</p>
